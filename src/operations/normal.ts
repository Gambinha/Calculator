
class NormalOperations {
    sum(n1: number, n2: number) {
        return n1 + n2;
    }

    subtraction(n1: number, n2: number) {
        return n1 - n2;
    }

    multiplication(n1: number, n2: number) {
        return n1 * n2;
    }

    division(n1: number, n2: number) {
        return n1 / n2;
    }
}

export {NormalOperations};